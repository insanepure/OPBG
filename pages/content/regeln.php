    <center><h3>Jeder User ist verpflichtet sich an diese Regeln zu halten!</h3></center>
    <div class="charname"><b><u>§1 Allgemeines</u></b></div>
     <div class="spacer"></div>
      1.1 Das Umgehen von Regeln ist verboten und selbst der Versuch ist verboten und kann zum Ausschluss von OPBG führen.
     <div class="spacer"></div>
      1.2 Das Verfälschen seiner IP-Adresse ist in jeglicher Art und Weise, wie zum Beispiel durch eine VPN, verboten.
     <div class="spacer"></div>
      1.3 OPBG ist ein deutschsprachiges Browsergame und das Team behält sich vor, dass sowohl auf dem Discord Server als auch auf der Webseite des Browsergames in deutsch kommuniziert wird.
     <div class="spacer"></div>
      1.4 Verfehlungen auf dem Discord Server können ebenfalls eine Auswirkung auf den Account/Charakter im Spiel haben.
     <div class="spacer"></div>
      1.5 Es ist verboten sich als eine andere Person oder als Team-Mitglied auszugeben.
     <div class="spacer"></div>
      1.6 Jeder User ist dazu verpflichtet Regelverstöße, welche er mitbekommt an das Team zu melden. Sollte dies wissentlich nicht geschehen behält sich das Team vor den User zu verwarnen oder zu bannen.
     <div class="spacer"></div>
      1.7 Eine Entbannung seines Accounts/Charakters wird ausschließlich von einem Direktor oder dem Admin durchgeführt.
    <div class="spacer"></div>
      1.8 Bei anliegen ans Team ist vorzugsweise das Ticketsystem zu nutzen.
      <div class="spacer"></div>
      1.9 Regeln die im selben Block stehen ergänzen sich ausnahmslos untereinander.
      <div class="spacer"></div>
      1.10 Das Nutzen von sogenannten Wegwerf-Email Adressen ist zu unterlassen.
      <div class="spacer"></div>
      1.11 Sollte zwischen zwei Banden eine Allianz abgesprochen sein, so muss diese auch über das System geschlossen werden.
      <div class="spacer"></div>
    <br /><hr><br /><div class="charname"><b><u>§2 PvP Kämpfe</u></b></div>
     <div class="spacer"></div>
      2.1 Es ist untersagt Kämpfe absichtlich zu verlieren.
     <div class="spacer"></div>
      2.2 Es ist untersagt sich in einem Kampf selber zu kicken.
     <div class="spacer"></div>
      2.3 Das unnötige und absichtliche verlängern von Kämpfen ist verboten.
     <div class="spacer"></div>
      2.4 Das absichtliche AFK gehen während eines Kampfes ist zu unterlassen.<br>
      Ausnahmeregel: Das absichtliche AFK gehen in PvP- und Elo-Kämpfe ist zwischen 12:00 und 00:00 Uhr zu unterlassen.<br />
      Das absichtliche AFK gehen in PvP- und Elo-Kämpfen um mögliche Kill-Techniken zu umgehen ist weiterhin verboten.
      <br><span style="color:red;">Ergänzt am 26.03.2023 um 12:30</span>
     <div class="spacer"></div>
      2.5 Der Angriff auf sich selbst ist verboten.
     <div class="spacer"></div>
      <br /><hr><br /><div class="charname"><u>2.6 PvP Kämpfe</u></div>
     <div class="spacer"></div>
      2.6.1 In einem PvP Kampf ist es gestattet ab Runde 30 aufzugeben, sofern der Gegner noch mehr als 50% seiner Lebenspunkte übrig hat und man selbst weniger als 50% Leben hat.
      <div class="spacer"></div>
      2.6.2 Die ersten 5 PvP Kämpfe am Tag dürfen ohne angezogene Rüstung durchgeführt werden.
      <div class="spacer"></div>
      <br /><hr><br /><div class="charname"><u>2.7 Elo Kämpfe</u></div>
      <div class="spacer"></div>
      2.7.1 Das Absichtliche herbeiführen eines Unentschiedens ist verboten.
      <div class="spacer"></div>
      2.7.2 Pro Tag ist es erlaubt maximal 2 Elo Kämpfe mit Mitgliedern derselben Bande oder mit Mitgliedern der Allianzbande durchzuführen.
      <div class="spacer"></div>
      2.7.3 Elo Kämpfe dürfen nur mit 100% LP und AD und dem Tragen seiner besten Rüstung gemacht werden.
      <div class="spacer"></div>
      <div class="charname"><u>2.8 Territoriale Kämpfe der Banden</u></div>
      <div class="spacer"></div>
      2.8.1 Man darf erst 5 Tage nach Beitritt in eine Bande an einem Bandenkampf teilnehmen.
      <div class="spacer"></div>
      <br /><hr><br /><div class="charname"><b><u>§3 Multiaccounts/-charaktere</u></b></div>
     <div class="spacer"></div>
      3.1 Die Freigabe eines weiteren Accounts oder Charakters benötigt eine Erlaubnis von einem Administrator. Andernfalls ist dies ausnahmslos Verboten.
     <div class="spacer"></div>
      3.2 Ein 2. Account/Charakter kann nur nach Absprache für ein Mitglied des eigenen Haushaltes, beispielsweise Frau, Kinder oder WG-Mitbewohner, erstellt werden.
     <div class="spacer"></div>
      3.3 Nach Genehmigung steht man unter genauerer Beobachtungen.
     <div class="spacer"></div>
      3.4 Das Interagieren miteinander ist untersagt. Darunter fallen beispielsweise Kopfgeld oder Elo Kämpfe.
     <div class="spacer"></div>
      3.5 Sollten die Regeln nicht eingehalten werden kann die Genehmigung wieder entzogen werden und beide Accounts/Charakter haften für die Verstöße.
     <div class="spacer"></div>
      3.6 Das Nutzen eines Charakters von mehr als einer Person ist verboten.
     <div class="spacer"></div>
      3.7 Ein großes X am Ende des Charakternamen bleibt Teammitgliedern vorbehalten, es darf nicht durch normale Charaktere verwendet werden da es einen Team-Account markiert.
      <div class="spacer"></div>
      <br /><hr><br /><div class="charname"><u>3.7 Account/Charakter Sitting</u></div>
    <div class="spacer"></div>
      3.7.1 Es ist möglich sich eine Urlaubsvertretung zu suchen, welche den eigenen Charakter für die Zeit des Urlaubs übertragen bekommt.
      <div class="spacer"></div>
      3.7.2 Sollte der Sitter absichtlich den zu sittenden Charakter löschen, für Verwarnungen oder einen Ban absichtlich sorgen, so haftet sein eigener Account/Charakter dafür und ihm wird der zu sittende Charakter entzogen.
      <div class="spacer"></div>
      3.7.3 Das Team haftet nicht für Entscheidungen, die der Sitter für den zu sittenden Charakter trifft. Daher sucht euch jemanden dem ihr vertraut und der keinen Unfug mit euren Charakter anstellt.
      <div class="spacer"></div>
      3.7.4 Darüber haftet das Team nicht für jegliches nicht einhalt von Abmachungen zwischen Sitter und zu sittenden Charakter.
      <div class="spacer"></div>
      3.7.5 Der Sittende hat verantwortungsvoll mit dem zu sittenden Charakter umzugehen.
      <div class="spacer"></div>
      3.7.6 Es ist verboten, dass der Charakter des Sittenden und der zu sittende Charakter miteinander interagieren.
      <div class="spacer"></div>
      3.7.7 Die Teilnahme an Bandenkämpfe als Sitter eines Charakters ist mit den zu sittenden Charakter ist nicht gestattet.
      <div class="spacer"></div>
      <br /><hr><br /><div class="charname"><u>3.8 Account/Charakter Löschung</u></div>
      <div class="spacer"></div>
      3.8.1 Die Übertragung von Spielinhalten, wie Beispielsweise Rüstung, Berry oder Gold an andere ist verboten.
      <div class="spacer"></div>
      3.8.2 Wer längere Zeit abwesend ist und nicht am spielgeschehen teilnimmt hat sich ebenfalls an 3.8.1 zu halten.
      <div class="spacer"></div>
      3.8.3 Sollte ein User bevor er sich löscht Gold oder Berry in die Bande eingezahlt haben, so muss der Kapitän der Bande dies dem Team melden.
      <div class="spacer"></div>
    <br /><hr><br /><div class="charname"><b><u>§4 Account/Charakter Handel</u></b></div>
     <div class="spacer"></div>
      4.1 Der Handel, Tausch oder die Veräußerung von seinem Account oder Charakter ist verboten.
     <div class="spacer"></div>
      4.2 Der Tausch von Accounts/Charakteren führt zum Ausschluss des Spiels für beide Accounts/Charaktere.
    <div class="spacer"></div>
    <div class="charname"><b><u>§5 Bugs</u></b></div>
     <div class="spacer"></div>
      5.1 Das wissentliche Ausnutzen eines Bugs ist verboten.
     <div class="spacer"></div>
      5.2 Jeder User hat umgehend einen Bug ans Team zu melden.
    <div class="spacer"></div>
    <div class="charname"><b><u>§6 Bots</u></b></div>
     <div class="spacer"></div>
      6.1 Das Nutzen von Bots, welche beispielsweise das Klicken für einen selbst übernehmen, ist verboten.
     <div class="spacer"></div>
      6.2 Das Nutzen von Refreshern, welche die Webseite automatisch neu Laden, ist verboten.
     <div class="spacer"></div>
      6.3 Das Nutzen von Browser Add-Ons, wie beispielsweise Vimium, ist verboten.
    <div class="spacer"></div>
    <br /><hr><br /><div class="charname"><b><u>$7 Werbung</u></b></div>
     <div class="spacer"></div>
      7.1 Die Verlinkung von nicht genehmigten Webseiten ist verboten.
     <div class="spacer"></div>
      7.2 Das Werben von anderen Usern im Bezug auf Schleichwerbung ist verboten.
    <div class="spacer"></div>
    <br /><hr><br /><div class="charname"><b><u>§8 Spam </u></b></div>
     <div class="spacer"></div>
